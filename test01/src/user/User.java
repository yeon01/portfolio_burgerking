package user;

public class User {
	private String userID;
	private String userPassword;
	private String userPasswordcheck;
	private String userName;
	private String userBth;
	private String userGender;
	private String userH_ads;
	
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserPasswordcheck() {
		return userPasswordcheck;
	}
	public void setUserPasswordcheck(String userPasswordcheck) {
		this.userPasswordcheck = userPasswordcheck;
	}
	public String getUserBth() {
		return userBth;
	}
	public void setUserBth(String userBth) {
		this.userBth = userBth;
	}
	public String getUserGender() {
		return userGender;
	}
	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}
	public String getUserH_ads() {
		return userH_ads;
	}
	public void setUserH_ads(String userH_ads) {
		this.userH_ads = userH_ads;
	}
	
}
